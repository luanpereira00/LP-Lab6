
#!/bin/bash
# Script para criacao de diretorios para estruturacao do projeto

# Diretorio para armazenamento dos executaveis finais
mkdir -p bin

# Diretorio para armazenamento dos objetos
mkdir -p build

# Diretorio para armazenamento dos dados utilizados
mkdir -p data

# Diretorio para armazenamento de documentacao gerada automaticamente
mkdir -p doc

# Diretorio para armazenamento de arquivos de cabecalho (.h)
mkdir -p include

# Diretorio para armazenamento de bibliotecas a serem ligadas ao programa
mkdir -p lib

# Diretorio para armazenamento de arquivos de corpo (.cpp)
mkdir -p src

# Diretorio para armazenamento de codigos fonte destinados a teste
mkdir -p test